function new_result = new(x, y)
% NEW  基于逐点相对比值的差异度指标
%
% 功能说明：
%   本函数用于衡量两个等长信号（或向量）在逐点意义下的
%   “相对差异程度”。其核心思想是：
%     1) 先计算对应元素的比值 q = x ./ y；
%     2) 以 (1 - q) 表示相对偏离程度；
%     3) 对所有点的偏离平方取均值；
%     4) 最后开平方，得到一个类似 RMS（均方根）的差异度指标。
%
%   当两个信号完全一致（x == y）时：
%       q = 1 → new_result = 0
%   差异越大，new_result 越大。
%
% 输入参数：
%   x, y : 数值向量
%          两个需要比较的信号（如归一化分段面积）
%
% 输出参数：
%   new_result : 标量
%          表示 x 与 y 之间的差异度，非负值
%
% 注意事项：
%   - x 和 y 必须长度一致；
%   - y 中不能包含 0（否则会导致除零问题）；
%   - 本指标对比例偏差较敏感，适用于“相对变化”分析场景。
%

    %% ==================== 输入合法性检查 ====================
    % 判断两个输入信号的长度是否一致
    if length(x) ~= length(y)
        error('两个信号长度必须相同');
    end

    %% ==================== 向量格式统一 ====================
    % 无论输入是行向量还是列向量，都统一转换为行向量
    % 这样可以避免后续计算中因维度不一致导致的错误
    A = x(:)';    % 将 x 强制转换为行向量
    B = y(:)';    % 将 y 强制转换为行向量

    %% ==================== 逐点相对差异计算 ====================
    % 计算逐点比值：
    %   q(i) = A(i) / B(i)
    % 若 A(i) == B(i)，则 q(i) = 1
    q = A ./ B;

    % 计算相对偏离量的平方：
    %   (1 - q)^2
    % 该项描述 A 相对于 B 的相对误差大小
    m = (1 - q).^2;

    %% ==================== 差异度汇总 ====================
    % 信号长度
    n = length(A);

    % 对所有点的相对偏离平方求平均，再开平方
    % 得到整体差异度（RMS 形式）
    new_result = sqrt(sum(m) / n);

end
